import re

num = ['2124567890',
'212-456-7890',
'(212)456-7890',
'(212)-456-7890',
'212.456.7890',
'212 456 7890',
'+12124567890',
'+12124567890',
'+1 212.456.7890',
'+212-456-7890',
'1-212-456-7890',
'01273','+198212']

patt = re.compile("\+?(?:\+?(\d{1,2}))?[-. (]*(\d{3})[-. )]*(\d{3})[-. ]*(\d{4})\s*")

for l in range(len(num)) :
    if patt.match(str(num[l])) :
        print(num[l]+" is valid")
    else :
        print(num[l]+" is invalid")
